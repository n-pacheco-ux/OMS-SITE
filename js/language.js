// Set default language
let currentLanguage = 'en';

// Function to update page content based on language
function updateLanguage(language) {
    currentLanguage = language;
    
    // Save language preference to local storage
    localStorage.setItem('language', language);
    
    // Update all elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[language][key]) {
            // Handle different elements appropriately
            if (element.tagName === 'INPUT' && element.type === 'submit') {
                element.value = translations[language][key];
            } else if (element.tagName === 'INPUT' && element.type === 'placeholder') {
                element.placeholder = translations[language][key];
            } else if (element.tagName === 'META' && element.getAttribute('name') === 'description') {
                element.setAttribute('content', translations[language][key]);
            } else if (element.tagName === 'IMG') {
                // For images, update the alt text, not the src
                element.alt = translations[language][key];
            } else if (element.tagName === 'A' && element.getAttribute('data-i18n-title')) {
                // Handle elements with both text and title attributes
                element.title = translations[language][element.getAttribute('data-i18n-title')];
                element.textContent = translations[language][key];
            } else {
                // Default: update text content
                element.textContent = translations[language][key];
            }
        }
    });
    
    // Update language flags to show active language
    document.querySelectorAll('.language-selector a').forEach(flag => {
        if (flag.getAttribute('data-lang') === language) {
            flag.classList.add('active-lang');
        } else {
            flag.classList.remove('active-lang');
        }
    });
    
    // Log language change for debugging
    console.log('Language changed to: ' + language);
}

// Initialize language on page load
document.addEventListener('DOMContentLoaded', () => {
    // Check if user has previously selected a language
    const savedLanguage = localStorage.getItem('language');
    if (savedLanguage) {
        currentLanguage = savedLanguage;
    }
    
    // Set up language switcher click events
    document.querySelectorAll('.language-selector a').forEach(flag => {
        flag.addEventListener('click', (e) => {
            e.preventDefault();
            const language = flag.getAttribute('data-lang');
            updateLanguage(language);
        });
    });
    
    // Initial language update
    updateLanguage(currentLanguage);
});
